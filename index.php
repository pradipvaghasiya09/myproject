				<h3><b style="color:white;">This article is about the city of Pune..<hr></b></h3><br>
				
				<img src="pune.jpg" align="left" width="30%" style="margin-right:10px;" 
						class="img-responsive img-thumbnail">
				<p style="padding:5px; text-align:justify;" ><br> 
				&emsp;&emsp;&emsp; 
						Pune is the 9th most populous city in India and the second largest in the state of Maharashtra after 
						the state capital Mumbai. Pune is also the 101st largest city in the world, by population.
						It is situated 560 metres (1,837 feet) above sea level on the Deccan plateau, on the right 
						bank of the Mutha river. Pune city is the administrative headquarters of Pune district 
						and was once the centre of power of the Maratha Empire established by Shivaji Maharaj.
						In the 18th century, Pune became the political centre of the Indian subcontinent, as the seat 
						of the Peshwas who were the prime ministers of the Maratha Empire.</p>
				<p style="text-align:justify;">
					&emsp;&emsp;&emsp; 
					Pune is considered the cultural capital of Maharashtra. Since the 1950s and 1960s, Pune has had 
					a traditional old-economic base. Most of the old industries continue to grow. The city is also known 
					for its manufacturing and automobile industries, as well as for research institutes of information 
					technology (IT), education, management and training, which attracts migrants, students, and professionals
					from India, South East Asia, the Middle East, and Africa. Pune is also one of the fastest growing cities in 
					the Asia-Pacific region. The ‘Mercer 2015 Quality of Living rankings’ evaluated local living conditions in more
					than 440 cities around the world where Pune ranked at 145, second in India after Hyderabad (138). It also highlights 
					Pune among evolving business centres and emerging 9 cities around the world with citation "Hosts IT and automotive companies".
				</p>
				<p style="text-align:justify;">
				&emsp;&emsp;&emsp;
					Copper plates dated 858 AD and 868 AD show that by the 8th century an agricultural settlement known as Punnaka existed where 
					Pune is today.[citation needed] The plates indicate that this region was ruled by the Rashtrakuta dynasty.
					The Pataleshwar rock-cut temple complex was built during this era
				<p>
				<p style="text-align:justify;">
				&emsp;&emsp;&emsp;
					Pune is 560 m (1,840 ft) above sea level on the western margin of the Deccan plateau. It is on the leeward side of the Sahyadri
					mountain range, which forms a barrier from the Arabian Sea. It is a hilly city, with its highest hill, Vetal Hill, rising to 800 m 
					(2,600 ft) above sea level. Just outside the city, the Sinhagad fort is at an altitude of 1,300 m. It lies between 18° 32" North 
					latitude and 73° 51" East longitude.Central Pune is at the confluence of the Mula and Mutha Rivers. The Pavana and Indrayani Rivers,
					tributaries of the Bhima River, traverse the northwestern outskirts of metropolitan Pune.
				</p>
